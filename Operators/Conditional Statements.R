#if statement, The if statement sets a condition, and if the condition evaluates to TRUE, the R code associated with the if statement is executed.
x <- 4 
if (x > 0) {
  print("x is a positive number")
}

#else statement.The else statement is used in combination with an if statement. This is how the code is structured in R: 
x <- 7

if (x > 0) {
  
  print ("x is a positive number")
  
} else {
  
  print ("x is either a negative number or zero")
  
}

x <- -7

if (x > 0) {
  
  print("x is a positive number")
  
} else {
  
  print ("x is either a negative number or zero")
  
}

#else if statement. In some cases, you might want to customize your conditional statement even further by adding the else if statement. The else if statement comes in between the if statement and the else statement. This is the code structure: 
x <- -1

if (x < 0) {
  
  print("x is a negative number")
  
} else if (x == 0) {
  
  print("x is zero")
  
} else {
  
  print("x is a positive number")
  
}
